package starpattern;

public class pattern1 {
	
	public static void main(String[] args) {
		
		
		for(int i=4;i>=1;i--) {
			
			
			for(int j=1;j<=5;j++) {
				
				System.out.print("*");
			
				for(int k=5;k>=1;k--) {
					
					System.out.print("*");
				}
				System.out.print("*");
			}
			System.out.println();
			
		}
		
		
		
	}
	
	
	
	

}
